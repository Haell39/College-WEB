// define uma função que recebe dois números e retorna a soma deles
function sum(a, b) {
  return a + b; // aqui está a soma dos dois números, a e b
}

// cria uma constante e atribui a ela o valor da função sum que foi executada
// com os valores 89 e 74, que são os dois números que foram passados como parâmetros
const value = sum(89, 74); // aqui está o resultado da função sum, que é 163

// imprime o valor da constante value no console, que é 163
console.log(value); // aqui está o valor da constante value, que é 163

